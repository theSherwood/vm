/* SQLite Phase A at corpus scale — a compact **sqllogictest runner** as a guest program.
 *
 * Reads a sqllogictest script (https://sqlite.org/sqllogictest/ — SQLite's own SQL Logic Tests,
 * the ≈600:1 validation corpus) from **stdin**, runs every record against an in-memory SQLite
 * (the same deterministic `SQLITE_OS_OTHER` build as `sqlite_demo.c`), and checks each query's
 * results against the expected values baked into the script — including the `N values hashing to
 * <md5>` form, via the embedded RFC-1321 MD5 below. Prints one `FAIL` line per mismatch and a
 * final summary, so the run is **self-validating** (failures must be 0: SQLite's own expected
 * results hold in the sandbox) *and* a clean **differential** (native build over the same stdin
 * must produce byte-identical output).
 *
 * Format notes (matching the reference `sqllogictest.c` runner byte-for-byte, since the corpus
 * hashes were generated by it):
 *   - value formatting: NULL → "NULL"; 'I' → "%d" of sqlite3_column_int (32-bit, as the reference
 *     does); 'R' → "%.3f"; 'T' → column text with "" → "(empty)" and chars outside 0x20..0x7e → '@';
 *   - `rowsort` orders whole rows, `valuesort` the flattened values, both before hashing;
 *   - the MD5 input is each value followed by "\n";
 *   - `skipif sqlite` / `onlyif <other-db>` records are skipped, `halt` stops the run.
 */

#define SQLITE_THREADSAFE 0
#define SQLITE_OMIT_LOAD_EXTENSION 1
#define SQLITE_OMIT_DEPRECATED 1
#define SQLITE_OMIT_WAL 1
#define SQLITE_OMIT_LOCALTIME 1
#define SQLITE_TEMP_STORE 3
#define SQLITE_OS_OTHER 1
#define SQLITE_DEFAULT_MEMSTATUS 0
#define SQLITE_MAX_MMAP_SIZE 0
#define SQLITE_DQS 0
#define NDEBUG 1

#include "sqlite3.c"

